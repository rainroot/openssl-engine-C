void DL (const unsigned char *i, unsigned char *o)
{
    unsigned char T;

    T = i[ 3] ^ i[ 4] ^ i[ 9] ^ i[14];
    o[ 0] = i[ 6] ^ i[ 8] ^ i[13] ^ T;
    o[ 5] = i[ 1] ^ i[10] ^ i[15] ^ T;
    o[11] = i[ 2] ^ i[ 7] ^ i[12] ^ T;
    o[14] = i[ 0] ^ i[ 5] ^ i[11] ^ T;
    T = i[ 2] ^ i[ 5] ^ i[ 8] ^ i[15];
    o[ 1] = i[ 7] ^ i[ 9] ^ i[12] ^ T;
    o[ 4] = i[ 0] ^ i[11] ^ i[14] ^ T;
    o[10] = i[ 3] ^ i[ 6] ^ i[13] ^ T;
    o[15] = i[ 1] ^ i[ 4] ^ i[10] ^ T;
    T = i[ 1] ^ i[ 6] ^ i[11] ^ i[12];
    o[ 2] = i[ 4] ^ i[10] ^ i[15] ^ T;
    o[ 7] = i[ 3] ^ i[ 8] ^ i[13] ^ T;
    o[ 9] = i[ 0] ^ i[ 5] ^ i[14] ^ T;
    o[12] = i[ 2] ^ i[ 7] ^ i[ 9] ^ T;
    T = i[ 0] ^ i[ 7] ^ i[10] ^ i[13];
    o[ 3] = i[ 5] ^ i[11] ^ i[14] ^ T;
    o[ 6] = i[ 2] ^ i[ 9] ^ i[12] ^ T;
    o[ 8] = i[ 1] ^ i[ 4] ^ i[15] ^ T;
    o[13] = i[ 3] ^ i[ 6] ^ i[ 8] ^ T;
}

void RotXOR (const unsigned char *s, int n, unsigned char *t)
{
    int i, q;

    q = n/8; n %= 8;
    for (i = 0; i < 16; i++) {
        t[(q+i) % 16] ^= (s[i] >> n);
        if (n != 0) t[(q+i+1) % 16] ^= (s[i] << (8-n));
    }
}

int ARIA_set_encrypt_key(const unsigned char *userKey, int bits, ARIA_KEY_SCHEDULE *key)
{
    int  i, R=(bits+256)/32, q;
    unsigned char t[ARIA_BLOCK_SIZE], w1[ARIA_BLOCK_SIZE], w2[ARIA_BLOCK_SIZE], w3[ARIA_BLOCK_SIZE];
    unsigned char *uk = (unsigned char *)userKey;
    unsigned char *rk = &key->rd_key[0];

    key->rounds = R;

    if (!userKey || !key)
        return -1;
    if (bits != 128 && bits != 192 && bits != 256)
        return -2;

    q = (bits - 128) / 64;
    for (i = 0; i < ARIA_BLOCK_SIZE; i++) t[i] = S[i % 4][KRK[q][i] ^ uk[i]];
    DL (t, w1);
    if (R==14)
        for (i = 0; i <  8; i++) w1[i] ^= uk[16+i];
    else if (R==16)
        for (i = 0; i < 16; i++) w1[i] ^= uk[16+i];

    q = (q==2)? 0 : (q+1);
    for (i = 0; i < ARIA_BLOCK_SIZE; i++) t[i] = S[(2 + i) % 4][KRK[q][i] ^ w1[i]];
    DL (t, w2);
    for (i = 0; i < ARIA_BLOCK_SIZE; i++) w2[i] ^= uk[i];

    q = (q==2)? 0 : (q+1);
    for (i = 0; i < ARIA_BLOCK_SIZE; i++) t[i] = S[i  % 4][KRK[q][i] ^ w2[i]];
    DL (t, w3);
    for (i = 0; i < ARIA_BLOCK_SIZE; i++) w3[i] ^= w1[i];

    for (i = 0; i < ARIA_BLOCK_SIZE*(R+1); i++) rk[i] = 0;

    RotXOR (uk, 0, rk +   0); RotXOR (w1,  19, rk +   0);
    RotXOR (w1, 0, rk +  16); RotXOR (w2,  19, rk +  16);
    RotXOR (w2, 0, rk +  32); RotXOR (w3,  19, rk +  32);
    RotXOR (w3, 0, rk +  48); RotXOR (uk,  19, rk +  48);
    RotXOR (uk, 0, rk +  64); RotXOR (w1,  31, rk +  64);
    RotXOR (w1, 0, rk +  80); RotXOR (w2,  31, rk +  80);
    RotXOR (w2, 0, rk +  96); RotXOR (w3,  31, rk +  96);
    RotXOR (w3, 0, rk + 112); RotXOR (uk,  31, rk + 112);
    RotXOR (uk, 0, rk + 128); RotXOR (w1,  67, rk + 128);
    RotXOR (w1, 0, rk + 144); RotXOR (w2,  67, rk + 144);
    RotXOR (w2, 0, rk + 160); RotXOR (w3,  67, rk + 160);
    RotXOR (w3, 0, rk + 176); RotXOR (uk,  67, rk + 176);
    RotXOR (uk, 0, rk + 192); RotXOR (w1,  97, rk + 192);
    if (R > 12)
    {
        RotXOR (w1, 0, rk + 208); RotXOR (w2,  97, rk + 208);
        RotXOR (w2, 0, rk + 224); RotXOR (w3,  97, rk + 224);
    }
    if (R > 14)
    {
        RotXOR (w3, 0, rk + 240); RotXOR (uk,  97, rk + 240);
        RotXOR (uk, 0, rk + 256); RotXOR (w1, 109, rk + 256);
    }
    return 0;
}

int ARIA_set_decrypt_key(const unsigned char *userKey, int bits, ARIA_KEY_SCHEDULE *key)
{
    int  i, j;
    unsigned char t[ARIA_BLOCK_SIZE];
    unsigned char *uk = (unsigned char *)userKey;
    unsigned char *rk = &key->rd_key[0];

    ARIA_set_encrypt_key(uk, bits, key);
    for (j = 0; j < ARIA_BLOCK_SIZE; j++)
    {
        t[j] = rk[j];
        rk[j] = rk[ARIA_BLOCK_SIZE*key->rounds + j];
        rk[ARIA_BLOCK_SIZE*key->rounds + j] = t[j];
    }
    for (i = 1; i <= key->rounds/2; i++)
    {
        DL (rk + i*ARIA_BLOCK_SIZE, t);
        DL (rk + (key->rounds-i)*ARIA_BLOCK_SIZE, rk + i*ARIA_BLOCK_SIZE);
        for (j = 0; j < ARIA_BLOCK_SIZE; j++) rk[(key->rounds-i)*ARIA_BLOCK_SIZE + j] = t[j];
    }
    return 0;
}

void ARIA_encrypt(const unsigned char *in, unsigned char *out, ARIA_KEY_SCHEDULE *key, const int enc)
{
	if(enc){}
    int i, j;
    unsigned char t[ARIA_BLOCK_SIZE];
    unsigned char *e = &key->rd_key[0];

    for (j = 0; j < ARIA_BLOCK_SIZE; j++) out[j] = in[j];
    for (i = 0; i < key->rounds/2; i++)
    {
        for (j = 0; j < ARIA_BLOCK_SIZE; j++) t[j] = S[     j  % 4][e[j] ^ out[j]];
        DL(t, out); e += ARIA_BLOCK_SIZE;
        for (j = 0; j < ARIA_BLOCK_SIZE; j++) t[j] = S[(2 + j) % 4][e[j] ^ out[j]];
        DL(t, out); e += ARIA_BLOCK_SIZE;
    }
    DL(out, t);
    for (j = 0; j < 16; j++) out[j] = e[j] ^ t[j];
}

int aria_init_key(EVP_CIPHER_CTX *ctx, const unsigned char *key, const unsigned char *iv, int enc)
{
	if(iv){}
    fprintf(stderr, "(ENGINE_OPENSSL_ARIA) aria_init_key() called\n");

    int ret;

    if ((ctx->cipher->flags & EVP_CIPH_MODE) == EVP_CIPH_CFB_MODE
        || (ctx->cipher->flags & EVP_CIPH_MODE) == EVP_CIPH_OFB_MODE
        || enc)
        ret=ARIA_set_encrypt_key(key, ctx->key_len * 8, ctx->cipher_data);
    else
        ret=ARIA_set_decrypt_key(key, ctx->key_len * 8, ctx->cipher_data);

    if(ret < 0)
    {
        EVPerr(EVP_F_ARIA_INIT_KEY,EVP_R_ARIA_KEY_SETUP_FAILED);
        return 0;
    }

    return 1;
}

int aria_ecb_cipher(EVP_CIPHER_CTX *ctx, unsigned char *out, const unsigned char *in, size_t inl)
{
    fprintf(stderr, "(ENGINE_OPENSSL_ARIA) aria_ecb_cipher() called\n");

    size_t i, bl;
    EOPENSSL_ARIA_KEY *dat = (EOPENSSL_ARIA_KEY *) ctx->cipher_data;

    bl = ctx->cipher->block_size;

    if(inl < bl)
        return 1;

    inl -= bl;

    for(i=0; i <= inl; i+=bl) {
		ARIA_encrypt(in + i, out + i, &dat->ks, ctx->encrypt);
	}

    return 1;
}

void ARIA_cbc_encrypt(const unsigned char *in, unsigned char *out, const unsigned long length, ARIA_KEY_SCHEDULE *key, unsigned char *ivec, const int enc)
{
    unsigned long n;
    unsigned long len = length;
    unsigned char tmp[ARIA_BLOCK_SIZE];
    const unsigned char *iv = ivec;

    if (ARIA_ENCRYPT == enc)
    {
        while (len >= ARIA_BLOCK_SIZE)
        {
            for(n=0; n < ARIA_BLOCK_SIZE; ++n)
                out[n] = in[n] ^ iv[n];
            ARIA_encrypt(out, out, key, enc);
            iv = out;
            len -= ARIA_BLOCK_SIZE;
            in += ARIA_BLOCK_SIZE;
            out += ARIA_BLOCK_SIZE;
        }
        if (len)
        {
            for(n=0; n < len; ++n)
                out[n] = in[n] ^ iv[n];
            for(n=len; n < ARIA_BLOCK_SIZE; ++n)
                out[n] = iv[n];
            ARIA_encrypt(out, out, key, enc);
            iv = out;
        }
        memcpy(ivec,iv,ARIA_BLOCK_SIZE);
    }
    else if (in != out)
    {
        while (len >= ARIA_BLOCK_SIZE)
        {
            ARIA_encrypt(in, out, key, enc);
            for(n=0; n < ARIA_BLOCK_SIZE; ++n)
                out[n] ^= iv[n];
            iv = in;
            len -= ARIA_BLOCK_SIZE;
            in  += ARIA_BLOCK_SIZE;
            out += ARIA_BLOCK_SIZE;
        }
        if (len)
        {
            ARIA_encrypt(in, tmp, key,enc);
            for(n=0; n < len; ++n)
                out[n] = tmp[n] ^ iv[n];
            iv = in;
        }
        memcpy(ivec,iv,ARIA_BLOCK_SIZE);
    }
    else
    {
        while (len >= ARIA_BLOCK_SIZE)
        {
            memcpy(tmp, in, ARIA_BLOCK_SIZE);
            ARIA_encrypt(in, out, key, enc);
            for(n=0; n < ARIA_BLOCK_SIZE; ++n)
                out[n] ^= ivec[n];
            memcpy(ivec, tmp, ARIA_BLOCK_SIZE);
            len -= ARIA_BLOCK_SIZE;
            in += ARIA_BLOCK_SIZE;
            out += ARIA_BLOCK_SIZE;
        }
        if (len)
        {
            memcpy(tmp, in, ARIA_BLOCK_SIZE);
            ARIA_encrypt(tmp, out, key, enc);
            for(n=0; n < len; ++n)
                out[n] ^= ivec[n];
            for(n=len; n < ARIA_BLOCK_SIZE; ++n)
                out[n] = tmp[n];
            memcpy(ivec, tmp, ARIA_BLOCK_SIZE);
        }
    }
}

int aria_cbc_cipher(EVP_CIPHER_CTX *ctx, unsigned char *out, const unsigned char *in, size_t inl)
{
    fprintf(stderr, "(ENGINE_OPENSSL_ARIA) aria_cbc_cipher() called\n");

    EOPENSSL_ARIA_KEY *dat = (EOPENSSL_ARIA_KEY *) ctx->cipher_data;

    while(inl>=EVP_MAXCHUNK)
    {
        ARIA_cbc_encrypt(in, out, (long)EVP_MAXCHUNK, &dat->ks, ctx->iv, ctx->encrypt);
        inl-=EVP_MAXCHUNK;
        in +=EVP_MAXCHUNK;
        out+=EVP_MAXCHUNK;
    }
    if (inl)
        ARIA_cbc_encrypt(in, out, (long)inl, &dat->ks, ctx->iv, ctx->encrypt);
    return 1;
}
